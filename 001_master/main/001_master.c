#define TOTAL_LINES (sizeof(hex_data)/sizeof(hex_data[0]))

// ===== INIT =====
void spi_master_init(void)
{
    spi_bus_config_t buscfg = {
        .mosi_io_num = PIN_MOSI,
        .miso_io_num = PIN_MISO,
        .sclk_io_num = PIN_CLK,
        .max_transfer_sz = 256,
    };

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 10000,
        .mode = 0,
        .spics_io_num = PIN_CS,
        .queue_size = 1,
        .cs_ena_posttrans = 3,
    };

    spi_bus_initialize(HSPI_HOST, &buscfg, SPI_DMA_CH_AUTO);
    spi_bus_add_device(HSPI_HOST, &devcfg, &spi);
}

// ===== SEND LINE =====
void spi_send_line(const char *line)
{
    char buffer[128];
    snprintf(buffer, sizeof(buffer), "%s\n", line);

    spi_transaction_t t;
    memset(&t, 0, sizeof(t));

    t.length = strlen(buffer) * 8;
    t.tx_buffer = buffer;

    spi_device_transmit(spi, &t);
}

// ===== READ ACK =====
uint8_t spi_read_ack(void)
{
    uint8_t ack = 0xFF;
    uint8_t dummy = 0xFF;

    spi_transaction_t t;
    memset(&t, 0, sizeof(t));

    t.length = 8;
    t.tx_buffer = &dummy;
    t.rx_buffer = &ack;

    spi_device_transmit(spi, &t);

    return ack;
}

// ===== SEND + ACK =====
int send_line_with_ack(const char *line)
{
    for (int i = 0; i < MAX_RETRY; i++)
    {
        spi_send_line(line);

        vTaskDelay(pdMS_TO_TICKS(50));

        uint8_t ack = spi_read_ack();

        if (ack == 0x06)
        {
            printf("OK: %s\n", line);
            return 1;
        }
        else if (ack == 0x15)
        {
            printf("NACK -> retry\n");
        }
        else if (ack == 0xEE)
        {
            printf("STOP FROM STM32\n");
            check_stop = 1 ; 
            return -1;
        }

        vTaskDelay(pdMS_TO_TICKS(200));
    }

    return 0;
}

// ===== SEND FILE =====
void send_hex_file(void)
{
    printf("===== START OTA =====\n");

    vTaskDelay(pdMS_TO_TICKS(2000));

    for (int i = 0; i < TOTAL_LINES; i++)
    {
        int ret = send_line_with_ack(hex_data[i]);  

        if (ret == -1)
        {
            break;
        }

        if (ret == 0)
        {
            printf("FAILED -> STOP\n");
            break;
        }

        vTaskDelay(pdMS_TO_TICKS(30));
    }

    printf("===== DONE =====\n");
}

// ===== MAIN =====
void app_main(void)
{
    spi_master_init();

    vTaskDelay(pdMS_TO_TICKS(1000));

    while (1)
    {
       if (check_stop == 0)
       {
            send_hex_file();
       }
    else if (check_stop == 1)
    {
        break;
    }
        
        vTaskDelay(pdMS_TO_TICKS(5000));
    }
}